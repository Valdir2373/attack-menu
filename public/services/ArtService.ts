  private _terminal: ArtTerminal;

  constructor() {
    this._configService = new ConfigService();
    const cfg = this._configService.art;
    this.nameMenu = "\u2620  " + this._configService.menu.name;

    const cowsay = new ArtCowsay(cfg.cowsay.figure, cfg.cowsay.message, cfg.cowsay.delay);
    cowsay.setConfigReader(() => {
      this._configService.reload();
      const c = this._configService.art.cowsay;
      return { figure: c.figure, message: c.message, delay: c.delay };
    });

    this._terminal = new ArtTerminal();
    this._terminal
      .register(new ArtSexy(ArtService.ARTS, cfg.sexy.delay))
      .register(cowsay);

    if (cfg.default) {
      this._terminal.switchTo(cfg.default);
    }
  }

  get configService(): ConfigService {
    return this._configService;
  }

  get terminal(): ArtTerminal {
    return this._terminal;
  }

  getNameMenu(): string {
    return this.nameMenu;
  }

  getLogo(): string {
    return this.logo;
  }

  getRandomIndex(): number {
    return Math.floor(Math.random() * this.count);
  }

  getArtByIndex(index: number): string {
    return this._terminal.current().content;
  }

  sexyArt(): string {
    return this._terminal.next().content;
  }

  get count(): number {
    const provider = this._terminal.activeProvider;
    return provider ? provider.frames().length : 1;
  }

  get currentDelay(): number {
    return this._terminal.current().delay;
  }

  switchArt(name: string): boolean {
    return this._terminal.switchTo(name);
  }

  nextFrame(): string {
    return this._terminal.next().content;
  }
